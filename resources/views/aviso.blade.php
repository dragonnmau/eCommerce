<h3>1. Encargado del tratamiento de sus datos personales</h3>

Tech World Co. SA. de SV., con domicilio en Av. Uxamal 45, calle 2 en cancun, Quintana Roo 77530, es el encargado del tratamiento de sus datos personales en nombre y por cuenta del Responsable, es decir, la empresa que contrata a PwC para prestarle servicios profesionales en relación con el Sistema Integral de Denuncia.

Para el Encargado, el tratar sus datos por cuenta del Responsable de manera legítima resulta un tema prioritario. Este Aviso de Privacidad complementa cualesquiera avisos que el Responsable maneje.

<h3>2. Persona Responsable/Departamento de datos personales:</h3>

La empresa para la cual usted trabaja y/o presta y/o recibe servicios, en su carácter de Responsable, será quien en su respectivo Aviso de Privacidad, dé a conocer la persona/departamento encargado de la protección y tratamiento de sus datos personales, así como de la oportuna atención a el ejercicio de sus derechos de acceso, rectificación, oposición y rectificación de sus datos personales.

Por lo que respecta a nuestra organización, nos sujetamos a las políticas, lineamientos y avisos de privacidad que nos proporcione el Responsable, de conformidad con el contrato de prestación de servicios profesionales que al efecto tenemos celebrado con dicho Responsable.

<h3>3. Finalidades del tratamiento de los datos personales</h3>

El Encargado por cuenta del Responsable recaba sus datos para las siguientes finalidades:

Para recabar las denuncias que se formulen a través del Sistema Integral de Denuncia, en cualquiera de sus canales (sitio Web, correo electrónico o la línea telefónica de denuncia);
Para completar y enviar periódicamente al Responsable un reporte con las denuncias presentadas, a fin de que él de oportuna atención, seguimiento y solución; y
Para fines estadísticos.
<h3>4. Datos personales que recabamos</h3>

Recabamos sus datos personales por cuenta del Responsable de forma directa cuando usted mismo nos los proporciona, ya sea por teléfono, a través de correo electrónico o del sitio Web del Sistema Integral de Denuncia. Los datos que obtenemos pueden ser, entre otros los siguientes: nombre, puesto, teléfono, correo electrónico, entre otros.

<h3>5. Uso de Cookies y Web Beacons</h3>

Le informamos que no recabamos datos personales a través del uso de Cookies o Web Beacons, y otras tecnologías para obtener información personal de usted.

<h3>6. Opciones y medios para limitar el uso o divulgación de datos personales</h3>

Usted puede dejar de recibir mensajes por correo electrónico, impresos, por teléfono fijo o celular comunicándose con el Responsable de Datos Personales directamente, ya que PwC no manda mensajes a los titulares de los datos personales.

<h3>7. Medios para ejercer los derechos de acceso, rectificación, cancelación u oposición</h3>

Usted tiene el derecho de acceder a sus datos personales y a los detalles del tratamiento de los mismos, así como a rectificarlos en caso de ser inexactos o incompletos; cancelarlos cuando resulten ser excesivos o innecesarios para las finalidades que justificaron su obtención u oponerse al tratamiento de los mismos para fines específicos, comunicándose directamente con el Responsable.

Para mayor información, favor de contactar al Responsable de datos personales, en el correo electrónico que al efecto este señale en su Aviso de Privacidad.

<h3>8. Mecanismos y procedimientos para que el titular, en su caso, revoque su consentimiento en cualquier momento</h3>

En cualquier momento del tratamiento usted podrá revocar el consentimiento que ha otorgado para el tratamiento de sus datos, para ello es necesario que contacte al Responsable de protección de datos, donde le será indicado el procedimiento que debe seguir para realizar la revocación de su consentimiento.

<h3>9. Transferencias de datos personales dentro de México y al extranjero</h3>

PwC solamente transfiere sus datos personales a la empresa para la cual usted trabaja y/o presta y/o recibe servicios; en ningún caso transferimos su información personal a otros terceros sin su consentimiento, salvo las excepciones previstas en el artículo 37 de la Ley Federal de Protección de Datos Personales en Posesión de Particulares, así como a realizar esta transferencia en los términos que fija esa ley.

<h3>10. Medidas de seguridad implementadas</h3>

Para la protección de sus datos personales hemos instrumentados medidas de seguridad de carácter administrativo, físico y técnico con el objeto de evitar pérdidas, mal uso o alteración de su información.

<h3>11. Modificaciones al aviso de privacidad</h3>

Nos reservamos el derecho de efectuar en cualquier momento modificaciones o actualizaciones al presente aviso de privacidad.

Estas modificaciones estarán disponibles al público a través de los siguientes medios: (i) en nuestro sitio Web del Sistema Integral de Denuncia; o (ii) se las haremos llegar al último correo electrónico que nos haya proporcionado.

<h3>12. Derecho de promover los procedimientos de protección de derechos y de verificación que sustancia el Instituto</h3>

